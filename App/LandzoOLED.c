/*********************************************************** 
* 函数功能：显示14X16字体
* 函数名称：OLED_P14x16Str
* 入口参数：x :   行位置
*           y :  列位置
*          ch ： 字体数组
* 出口参数：无 
* 修改时间: 2014-08-18
* 备 注： 
***********************************************************/

void OLED_P14x16Str(uint8_t x,uint8_t y,uint8_t ch[])
{
  uint8_t wm=0,ii = 0;
  uint16_t adder=1;
  
  while(ch[ii] != '\0')
  {
    wm = 0;
    adder = 1;
    while(F14x16_Idx[wm] > 127)
    {
      if(F14x16_Idx[wm] == ch[ii])
      {
        if(F14x16_Idx[wm + 1] == ch[ii + 1])
        {
          adder = wm * 14;
          break;
        }
      }
      wm += 2;
    }
    if(x>118){x=0;y++;}
    OLED_Set_Pos(x , y);
    if(adder != 1)// 显示汉字
    {
      OLED_Set_Pos(x , y);
      for(wm = 0;wm < 14;wm++)
      {
        OLED_WrDat(F14x16[adder]);
        adder += 1;
      }
      OLED_Set_Pos(x,y + 1);
      for(wm = 0;wm < 14;wm++)
      {
        OLED_WrDat(F14x16[adder]);
        adder += 1;
      }
    }
    else			  //显示空白字符
    {
      ii += 1;
      OLED_Set_Pos(x,y);
      for(wm = 0;wm < 16;wm++)
      {
        OLED_WrDat(0);
      }
      OLED_Set_Pos(x,y + 1);
      for(wm = 0;wm < 16;wm++)
      {
        OLED_WrDat(0);
      }
    }
    x += 14;
    ii += 2;
  }
}

/*********************************************************** 
* 函数功能：数字转换成ACSII
* 入口参数：hex :   需要显示的数
*           Print : 转换后的数组指针
* 出口参数：无 
* 修改时间: 2014-08-18
* 备 注： 
***********************************************************/

void OLED_HEXACSII(uint16_t hex,uint8_t* Print)
{
  uint8_t hexcheck ;
  uint8_t TEMP ;
  TEMP = 6 ;
  Print[TEMP ]='\0';
  while(TEMP)
  {
    TEMP -- ;
    hexcheck  =  hex%10 ;
    hex   /=10 ;
    Print[TEMP ]  = hexcheck + 0x30 ;
  }
  
}

/*********************************************************** 
* 函数名称：OLED_Print_Num
* 函数功能：数字显示
* 入口参数：x : 角度显示字符
*           y : 加速度显示字符
*          num: 显示的数字
* 出口参数：无 
* 修改时间: 2014-08-18
* 备 注： 
***********************************************************/
void OLED_Print_Num(uint8_t x, uint8_t y, uint16_t num)
{
  uint8_t *ch1,ch[7];
  uint8_t ii=0;
  
  OLED_HEXACSII(num,ch);
  
  while(ch[ii]==0x30)
  {
      ii++;
  }
  
  ch1 =&ch[ii];
  
  while(*ch1 != '\0')
  {
      OLED_P8x16Str(x, y, ch1);	//显示数字
      x += 8;
     ch1++;
  }
}

/*********************************************************** 
* 函数名称：OLED_Print_Str
* 函数功能：字符显示
* 入口参数：x : 位置
*           y : 位置
*          ch: 显示的字符串数组
* 出口参数：无 
* 修改时间: 2014-08-18
* 备 注： 
***********************************************************/
void OLED_Print_Str(uint8_t x, uint8_t y, uint8_t ch[])
{
  uint8_t ch2[3];
  uint8_t ii=0;
  while(ch[ii] != '\0')
  {
    if(ch[ii] > 127)
    {
      ch2[0] = ch[ii];
      ch2[1] = ch[ii + 1];
      ch2[2] = '\0';			//汉字为两个字节
      OLED_P14x16Str(x , y, ch2);	//显示汉字
      x += 14;
      ii += 2;
    }
    else
    {
      ch2[0] = ch[ii];
      ch2[1] = '\0';			//字母占一个字节
      OLED_P8x16Str(x , y , ch2);	//显示字母
      x += 8;
      ii+= 1;
    }
  }
}

