/*************************************************************************
*                           蓝宙电子工作室
*
*  函数名称：OLED_WrDat
*  功能说明：写数据函数
*  参数说明： data
*  函数返回：无
*  修改时间：2014-9-13
*  备    注：
*************************************************************************/
void OLED_WrDat(uint8_t data)
{
  uint8_t i=8;
  
  OLED_DC=1;
  OLED_SCL=0;
  while(i--)
  {
    if(data&0x80){OLED_SDA=1;}
    else{OLED_SDA=0;}
    OLED_SCL=1;
    asm("nop");
    OLED_SCL=0;
    data<<=1;
  }
}

